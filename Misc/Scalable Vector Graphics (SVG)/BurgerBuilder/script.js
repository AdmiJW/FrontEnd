

//  The thud sound effect when falling burger components hit the bottom of svg window
const thudSfx = document.createElement('audio');
thudSfx.src = 'https://freesound.org/data/previews/122/122122_1449999-lq.mp3';

function playThud() {
    thudSfx.currentTime = 0;
    thudSfx.play();
}


//  Set up the transition and transform CSS property for the element, given the element,
//  duration of transition, position of the transform of translateY and timingFunction as the argument
function setTransitionTransform(element, duration, position, timingFunction) {
    const style = element.style;   
    style.transition = `transform ${duration} ${timingFunction}`;
    setTimeout(() => {
        style.transform = `translateY( ${position} )`;
    }, 50);
}

//  This function simulates the keyframes in CSS, essentially setting up a dropping effect with 2 rebounces
function setDropEffect(element, offsetY) {
    setTransitionTransform(element, '1s', `calc( 100% - ${offsetY}px`, 'ease-in');  //  Dropping from top of SVG
    setTimeout(() => {
        playThud();
        setTransitionTransform(element, '0.5s', ` calc(100% - ${offsetY + 75}px`, 'ease-out');  //Rebounce 1
        setTimeout(() => {
            setTransitionTransform(element, '0.5s', ` calc(100% - ${offsetY}px`, 'ease-in');    //  Dropping again
            setTimeout(() => {
                playThud();
                setTransitionTransform(element, '0.25s', ` calc(100% - ${offsetY + 25}px`, 'ease-out'); //  Rebounce 2
                setTimeout(() => {
                    setTransitionTransform(element, '0.25s', ` calc(100% - ${offsetY}px`, 'ease-in'); //Landing
                }, 250);
            }, 500);
        }, 500);
    }, 1000);
}


